/**
 * Manages the state of keyboard inputs for controlling the game.
 * This class tracks the state of different keys and allows for querying which keys are currently pressed.
 */
class Keyboard {
   /**
    * Indicates if the LEFT arrow key is pressed.
    * @type {boolean}
    * @default false
    */
   LEFT = false;

   /**
    * Indicates if the RIGHT arrow key is pressed.
    * @type {boolean}
    * @default false
    */
   RIGHT = false;

   /**
    * Indicates if the UP arrow key is pressed.
    * @type {boolean}
    * @default false
    */
   UP = false;

   /**
    * Indicates if the DOWN arrow key is pressed.
    * @type {boolean}
    * @default false
    */
   DOWN = false;

   /**
    * Indicates if the SPACE bar is pressed.
    * @type {boolean}
    * @default false
    */
   SPACE = false;

   /**
    * Indicates if the 'D' key is pressed.
    * @type {boolean}
    * @default false
    */
   D = false;
}
